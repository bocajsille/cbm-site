<div class="ib"><p>Camp ministry at Children's Bible Ministries is much more than seasonal programming — it is year-round gospel ministry. For those called to vocational ministry, serving at a CBM camp is an opportunity to invest in the next generation and help create environments where the Gospel is consistently taught and lived out throughout the entire year.</p></div>

<div class="s sw"><div class="si sin">
<span class="lb">More Than a Job</span><h2>A Calling, Not Just a Career</h2>
<p>Across CBM locations, camps serve as places where children, youth, families, churches, and ministry leaders encounter God's Word through camps, retreats, conferences, discipleship opportunities, and Christian community. Behind these ministries are faithful men and women who have dedicated their lives to serving Christ full-time through camp ministry.</p>
<p>Long-term camp ministry staff at CBM serve as missionaries — committed to the mission, supported by churches and prayer partners, and living out their calling through every season of the year.</p>
</div></div>

<div class="s sl"><div class="si sin">
<span class="lb">Roles &amp; Responsibilities</span><h2>What Long-Term Camp Ministry Looks Like</h2>
<p>Year-round camp ministry staff may serve in a wide variety of roles, including:</p>
<ul>
<li>Camp director or assistant director leadership</li>
<li>Year-round Bible teaching, discipleship, and program development</li>
<li>Retreat coordination and group ministry hosting</li>
<li>Facilities management and property stewardship</li>
<li>Food service and hospitality ministry</li>
<li>Fundraising, development, and donor relations</li>
<li>Administration, communications, and logistics</li>
<li>Family ministry and community outreach</li>
</ul>
<p>Every role contributes to the mission: creating Christ-honoring environments where God's Word is faithfully proclaimed throughout the year.</p>
</div></div>

