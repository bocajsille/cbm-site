<div class="s sl"><div class="si sin">
<span class="lb">Location</span>
<h2>Heart of the Smoky Mountains</h2>
<p>TRC is located in <strong>Townsend, Tennessee</strong>, nestled in the Great Smoky Mountains — just outside the National Park and only 20 minutes from Cades Cove. Surrounded by natural beauty, guests have access to:</p>
<ul>
<li>Hiking trails and waterfalls</li>
<li>Historical landmarks and scenic drives</li>
<li>Cycling and e-biking routes</li>
<li>Horseback riding and ziplining</li>
<li>Summer float trips on the Little River</li>
<li>Whitewater rafting within 1.5 hours</li>
</ul>
<p>Pigeon Forge (Dollywood, Splash Country) is 30 minutes away. Gatlinburg and Sevierville are also within a short drive.</p>
</div></div>

<div class="s sw"><div class="si sin">
<span class="lb">Group Lodging</span>
<h2>Lodging &amp; Group Accommodations</h2>
<p>TRC can accommodate over <strong>100 guests</strong> across seven cabins and lodges, making it ideal for church retreats, family reunions, ministry teams, high school or college retreats, corporate training events, and more. Many groups have returned year after year, calling TRC their "home away from home" for decades.</p>
<a href="https://www.tuckaleecheeretreatcenter.com" target="_blank" rel="noopener" class="la">Explore Cabins, Photos &amp; Book Online at TRC</a>
