<div class="two-col">
<div>
<span class="lb">Our Purpose</span>
<h2>A Place for Training, Rest, and Renewal</h2>
<p>Since acquiring the property in 2000, CBM has established TRC as a central gathering place for ministry, equipping, and spiritual renewal — especially for the CBM family throughout the eastern United States and beyond.</p>
<h3>1. Global Headquarters &amp; Training Center</h3>
<p>TRC is the hub for CBM ministry operations and leadership development. Throughout the year, CBM hosts key training and fellowship events including:</p>
<ul>
<li>G.R.O.W. (new missionary training)</li>
<li>CBM Directors Conference</li>
<li>CBM Programmers Conference</li>
<li>CBM Ladies Retreat</li>
<li>CBM Men's Retreat</li>
</ul>
<h3>2. A Place of Rest and Renewal</h3>
<p>TRC provides a peaceful setting for pastors, missionaries, families, church groups, and other guests to step away from the demands of daily life and spend intentional time with the Lord and with family.</p>
</div>
<div class="img-ph">[ Photo: TRC property / Smoky Mountains ]</div>
</div>
