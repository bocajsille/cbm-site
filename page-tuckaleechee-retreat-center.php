<div class="s sl"><div class="si sin">
<span class="lb">Pastoral &amp; Missionary Care</span>
<h2>A Retreat for Pastors and Missionaries</h2>
<p>One of TRC's primary purposes is to provide an affordable, accessible place of rest for pastors and missionaries. Most days of the year, at least one pastor or missionary family is staying on the property — seeking quiet time with the Lord and quality time with family in the solitude of the mountains.</p>
<p>To serve as many as possible, TRC offers a tiered pricing system:</p>
<div class="tier-box">
<h4>Tiered Pricing for Ministry Leaders</h4>
<p>Tier 1: First pastor/missionary to book a given date receives the deepest discount. Additional tiers allow others to stay while still supporting the ministry. This structure keeps TRC accessible while sustaining it as a ministry resource.</p>
</div>
