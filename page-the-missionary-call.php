<h1>The Missionary Call</h1>
</div></div>

<div class="cbm-intro"><p>Throughout Scripture, God calls ordinary people to participate in His work. Some are called to serve faithfully within their local church and community. Others are called into vocational ministry and missions — dedicating their lives to helping others hear and understand the Gospel. The missionary call is not primarily about position, travel, or recognition. It is about surrender, obedience, and a willingness to follow the Lord wherever He leads.</p></div>

<div class="s sw"><div class="si sin">
<span class="lbl">Scripture &amp; Calling</span>
<h2>God Uses Ordinary People</h2>
<p>Throughout the Bible, God consistently called ordinary people into His service. Moses questioned his ability. Gideon doubted himself. Jeremiah felt too young. The disciples were not religious elites or powerful leaders. Yet God used willing and surrendered people to accomplish His purposes.</p>
<p>The missionary call is not about having every answer or feeling fully prepared. More often, it begins with a growing burden, a willingness to obey, and a heart that is available to God.</p>
<p>Many people serving in ministry today once felt — or even still feel — uncertain, unqualified, inexperienced, or hesitant. Yet over time, the Lord used Scripture, Christian community, opportunities, and faithful obedience to clarify His leading.</p>
<div class="scripture">
<p>"The harvest truly is plenteous, but the labourers are few; Pray ye therefore the Lord of the harvest, that he will send forth labourers into his harvest."</p>
<cite>Matthew 9:37–38</cite>
</div>
</div></div>

<div class="s sl"><div class="si sin">
<span class="lbl">Ordinary Obedience</span>
<h2>Missionaries Are Ordinary People Who Said "Yes"</h2>
<p>Missionaries are not spiritual celebrities or people who have somehow "arrived" at a higher level of Christianity. They are ordinary believers who, over time, sensed God leading them toward vocational ministry and chose to respond in obedience.</p>
<p>Some first served as <a href="<?php echo home_url('/volunteer-opportunities');?>">volunteers</a>. Others came through <a href="<?php echo home_url('/christian-camps-camp-ministry');?>">camps</a>, <a href="<?php echo home_url('/internship-program');?>">internships</a>, <a href="<?php echo home_url('/serve-on-a-mission-trip');?>">mission trips</a>, church ministry, or ordinary acts of service. Most did not begin with a perfectly clear plan for their lives.</p>
<p>What set them apart was not perfection or extraordinary ability — it was a willingness to say "yes" to the Lord's leading, one step at a time.</p>
</div></div>

<div class="s sw"><div class="si sin">
<span class="lbl">Gradual Calling</span>
<h2>Calling Often Grows Through Faithful Service</h2>
<p>Many people do not experience a dramatic or immediate sense of calling. The Lord often uses one step of obedience to gradually shape and clarify His direction for someone's life through:</p>
<ul>
<li>Volunteering in local ministry</li>
<li>Serving at camp seasonally</li>
<li>Completing an internship</li>
<li>Going on a short-term mission trip</li>
<li>Church involvement and discipleship relationships</li>
</ul>
<p>A willingness to serve faithfully where you are today is often part of how God prepares you for what comes next.</p>
</div></div>

<div class="s sl"><div class="si sin">
<span class="lbl">Community &amp; Accountability</span>
<h2>Calling Is Confirmed in Community</h2>
<p>The missionary call is not meant to be pursued in isolation. Throughout Scripture, calling and sending are closely connected to the local church and the affirmation of spiritually mature believers.</p>
<p>At CBM, we believe healthy missionary service should include accountability, church partnership, spiritual counsel, discipleship, and confirmation within biblical community. The local church plays an important role in helping individuals discern and pursue ministry faithfully.</p>
<a href="<?php echo home_url('/church-partnerships');?>" class="la">Learn About Church Partnerships</a>
</div></div>

<div class="s sw"><div class="si sin">
<span class="lbl">Questions for Reflection</span>
<h2>Questions for Discernment</h2>
<p>Not everyone is called to vocational ministry or missionary service. But if the Lord may be stirring your heart toward ministry, these questions can be helpful places to begin praying and reflecting:</p>
<ul>
<li>Do you sense a growing burden for people to hear God's Word?</li>
<li>Are you willing to serve wherever the Lord leads?</li>
<li>Has your church been affirming gifts or ministry potential in your life?</li>
<li>Do opportunities for ministry continue to open before you?</li>
<li>Do you desire to invest your life in helping others know Christ?</li>
<li>Are you willing to surrender your plans if God leads differently than expected?</li>
</ul>
<p>Calling is rarely about comfort or personal ambition. More often, it is about availability, obedience, and faithfulness.</p>
</div></div>

<div class="s sl"><div class="si sin">
<span class="lbl">Stories</span>
<h2>Stories of Calling</h2>
<div class="quote-block"><p>"I originally planned to serve only one summer at camp, but over time the Lord used those experiences to grow a burden for ministry that I could not ignore."</p><cite>— CBM Missionary</cite></div>
<div class="quote-block"><p>"Through serving on mission trips and teaching children, I began to realize the Lord was calling me toward long-term ministry."</p><cite>— Former Intern</cite></div>
<div class="quote-block"><p>"My church and mentors recognized gifts in me long before I fully understood what the Lord might be doing. Their encouragement played a major role in helping me discern my calling."</p><cite>— CBM Staff Member</cite></div>
<div class="quote-block"><p>"The calling to ministry did not happen overnight. It developed gradually through years of prayer, service, and surrender."</p><cite>— Camp Missionary</cite></div>
</div></div>

<div class="s sw"><div class="si sin">
<span class="lbl">Next Steps</span>
<h2>Prayerfully Considering Next Steps</h2>
<p>If you believe the Lord may be leading you toward ministry or missions, you do not need to have everything figured out today. The next step may simply be:</p>
<ul>
<li>Committing the matter to fervent prayer</li>
<li>Seeking wise counsel from your pastor or mentor</li>
<li>Serving now where opportunities exist</li>
<li>Beginning a conversation with your pastor</li>
<li>Taking one step of obedience at a time</li>
</ul>
<p>God often provides clarity gradually as His people walk faithfully with Him.</p>
