<div class="ib"><p>Mission trips with CBM are opportunities to come alongside long-term missionaries, serve children and youth through gospel ministry, and take a meaningful step of obedience. Whether you're going individually or bringing your whole church, there's a place for you.</p></div>

<div class="s sw"><div class="si sin">
<span class="lb">Who Can Serve</span><h2>Who Can Go</h2>
<p>CBM mission trips are open to individuals, groups, families, and churches who are committed to serving under the direction of established missionaries. Participants should:</p>
<ul><li>Be followers of Christ with a sincere desire to serve</li><li>Be willing to work under the direction of long-term CBM missionaries</li><li>Come with a servant's heart, prepared to serve in whatever capacity is needed</li><li>Be 14 years of age or older (younger participants welcomed as part of a family team)</li></ul>
<p>Groups are welcome, and churches are encouraged to bring teams. If you're bringing a group, please reach out early to allow time for coordination with our missionary teams.</p>
</div></div>

<div class="s sl"><div class="si sin">
<span class="lb">What to Expect</span><h2>What a Mission Trip Looks Like</h2>
<p>Every trip is shaped by the specific ministry context and the needs of the missionary team you'll be serving. Generally, expect to:</p>
<h3>Ministry Activities</h3>
<ul><li>Assist with Bible teaching and children's programs</li><li>Support camp activities and retreat events</li><li>Help with facility work, maintenance, and improvement projects</li><li>Prayer walks and community outreach</li><li>Encourage and support long-term missionaries</li></ul>
<h3>Spiritual Experience</h3>
<ul><li>Daily devotions and team prayer</li><li>Hands-on gospel ministry</li><li>Mentorship from full-time missionaries</li><li>Personal spiritual growth through serving</li></ul>
<a href="<?php echo home_url('/mission-trips');?>" class="la">Learn More About CBM's Mission Trip Philosophy</a>
</div></div>

<div class="s sw"><div class="si sin">
<span class="lb">Cost &amp; Logistics</span><h2>Practical Information</h2>
<p>Mission trip costs vary depending on location, duration, and travel requirements. Domestic trips generally involve lower costs, while international trips require additional planning for travel, visas, and logistics. CBM staff will work with you to provide a cost overview specific to your trip.</p>
<p>All participants are expected to come prepared spiritually and practically. CBM will provide orientation information, packing recommendations, and any specific requirements for the ministry site prior to departure.</p>
</div></div>

