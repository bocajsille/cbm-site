<div class="s sw"><div class="si sin">
<span class="lb">Overview</span>
<h2>Scripture-Centered Teaching That Forms Character</h2>
<p>CBM's Character-Building Curriculum is designed to provide children and youth with consistent, Scripture-based instruction that shapes both biblical understanding and daily living. Rooted in God's Word, this curriculum teaches foundational truths while emphasizing character formation that reflects Christ.</p>
<p>Developed and written by CBM missionaries, the curriculum reflects both theological depth and real-world experience. It is actively used within CBM to teach Bible lessons in <a href="<?php echo home_url('/released-time-bible-education');?>">Bible Released Time programs</a> and Bible Clubs, ensuring that students receive clear, consistent instruction across multiple settings.</p>
<h3>Key Curriculum Distinctives</h3>
<ul>
<li><strong>Christ-Centered Teaching</strong> — Every lesson points students to Jesus Christ as the foundation for both salvation and daily living</li>
<li><strong>Consistent Biblical Instruction</strong> — Students receive regular, age-appropriate teaching that builds upon itself over time</li>
<li><strong>Practical Life Application</strong> — Lessons connect Scripture to real-life situations, helping students develop Christlike character</li>
<li><strong>Flexible Ministry Contexts</strong> — Adaptable for Bible Clubs, Released Time, camps, churches, homeschool settings, and international ministry</li>
</ul>
</div></div>

<div class="s sl"><div class="si sin">
<span class="lb">What Students Learn</span>
<h2>What Students Learn</h2>
<p>The Character-Building Curriculum emphasizes both knowledge and transformation. Core areas of focus include:</p>
<ul>
<li>Foundational Bible knowledge and understanding</li>
<li>The gospel message and salvation through Christ</li>
<li>Core Christian beliefs and doctrine</li>
<li>Christian character traits such as obedience, honesty, and kindness</li>
<li>Spiritual disciplines including prayer and Scripture memory</li>
<li>Living out faith in everyday life</li>
</ul>
</div></div>

