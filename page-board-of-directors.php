<div class="s sl"><div class="si sin">
<span class="lb">Responsibilities</span>
<h2>The Role of the Board</h2>
<p>The Board of Directors is responsible for providing governance and oversight for the ministry as a whole. Key responsibilities include:</p>
<ul>
<li>Protecting the mission and doctrinal integrity of the ministry</li>
<li>Providing strategic direction and organizational guidance</li>
<li>Supporting and evaluating executive leadership</li>
<li>Approving major policies and ministry initiatives</li>
<li>Ensuring responsible <a href="<?php echo home_url('/financials-financial-accountability');?>">financial stewardship</a></li>
<li>Safeguarding the long-term health of the ministry</li>
<li>Strengthening accountability throughout the organization</li>
</ul>
<h2 style="margin-top:2.5rem;">A Commitment to Biblical Governance</h2>
<p>CBM believes healthy ministry requires both strong leadership and meaningful accountability. The Board seeks to lead through biblical wisdom, prayerful decision-making, servant leadership, financial responsibility, doctrinal faithfulness, and long-term stewardship — values that have guided CBM <a href="<?php echo home_url('/history-timeline');?>">since 1935</a>.</p>
