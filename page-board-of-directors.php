<div class="ib"><p>Children's Bible Ministries is governed by a Board of Directors committed to ensuring that the ministry remains faithful to its mission, biblical convictions, and long-term calling — providing accountability, counsel, and oversight to help strengthen Gospel ministry for generations to come.</p></div>

<div class="s sw"><div class="si sin">
<span class="lb">Governance</span>
<h2>Faithful Oversight for Faithful Ministry</h2>
<p>The Board serves as a steward of the ministry's mission, resources, and future direction, working alongside the <a href="<?php echo home_url('/leadership-team');?>">Global Directors</a> to help ensure that Children's Bible Ministries remains focused on teaching God's Word to children and youth while operating with integrity, transparency, and responsible stewardship.</p>
<h2 style="margin-top:2.5rem;">Governance Throughout the CBM Family</h2>
<p>Children's Bible Ministries is a network of ministry locations and camps that share a common mission, Statement of Faith, and organizational relationship with CBM Global. Some ministries maintain their own local Boards of Directors, responsible for local governance, ministry operations, staffing, fundraising, and property stewardship.</p>
<p>The CBM Global Board provides oversight for the ministry as a whole while partnering with local Boards to preserve the mission, doctrine, and long-term vision of CBM.</p>
<a href="<?php echo home_url('/organizational-structure');?>" class="la">Learn About CBM's Organizational Structure</a>
