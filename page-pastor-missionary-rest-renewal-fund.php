<h1>Pastor and Missionary Rest &amp; Renewal Fund</h1>
</div></div>

<div class="ib"><p>Pastors and missionaries carry the spiritual, emotional, and relational weight of caring for others — often with few opportunities to step away for intentional rest. Many serve with limited financial resources, irregular schedules, and constant ministry demands that leave little margin for renewal.</p></div>

<div class="s sw"><div class="si sin">
<span class="lb">Why This Fund Exists</span>
<h2>Why Rest and Renewal Matter for Gospel Workers</h2>
<p>The Pastor and Missionary Rest &amp; Renewal Fund exists to provide pastors, missionaries, and at times their families with intentional time away in a Christ-centered environment — not for indulgence, but for prayer, rest, reflection, and spiritual strengthening. These stays are designed to help gospel workers return to their calling refreshed, refocused, and sustained for faithful service.</p>
<p class="statement">This is not about luxury. It is about stewardship of the people God has called to shepherd His Church.</p>
</div></div>

<div class="s sl"><div class="si sin">
<span class="lb">The Purpose</span>
<h2>What This Fund Supports</h2>
<p>The Pastor and Missionary Rest &amp; Renewal Fund supports the long-term sustainability of gospel workers by providing access to intentional periods of rest, prayer, and family strengthening in Christ-centered environments. These opportunities are especially critical for pastors and missionaries who serve with limited financial resources and few opportunities to step away from ministry demands.</p>
<p>Rather than operating as a revenue-generating program, this ministry depends on faithful stewardship and partner support to ensure that rest and renewal remain accessible. By investing in this fund, supporters strengthen not only individual leaders and families, but also the churches and communities they serve.</p>
<p>Sponsors are not funding a vacation; they are investing in:</p>
<ul>
<li>Spiritual health</li>
<li>Marital and family stability</li>
<li>Long-term ministry sustainability</li>
<li>Faithful endurance in gospel work</li>
</ul>
<p>When pastors and missionaries are strengthened, the churches and communities they serve are strengthened as well.</p>
</div></div>

<div class="s sw"><div class="si sin">
<span class="lb">Sustaining This Ministry</span>
<h2>How This Ministry Is Made Possible</h2>
<p>Because these stays are offered at deeply reduced rates — and sometimes at no cost — this ministry of rest and renewal depends on partners who believe in caring for those who serve the Church. Support from churches, individuals, and businesses allows Children's Bible Ministries to:</p>
<ul>
<li>Keep retreat spaces accessible to gospel workers</li>
<li>Maintain and steward facilities responsibly</li>
<li>Offer hospitality without placing financial strain on pastors or missionaries</li>
<li>Sustain this ministry for future generations of leaders</li>
</ul>
<p class="statement">This fund ensures that rest and renewal remain a gift — not a burden.</p>
</div></div>

<div class="s sl"><div class="si sin">
<span class="lb">Who Partners</span>
<h2>Who Can Partner in This Ministry</h2>
<p>The Pastor and Missionary Rest &amp; Renewal Fund is supported by:</p>
<ul>
<li>Churches seeking to care well for their pastors and missionaries</li>
<li>Individuals with a heart for strengthening gospel workers</li>
<li>Businesses looking to invest in meaningful, community-impacting ministry</li>
</ul>
<p>Every partnership — large or small — helps make rest and renewal possible for those who serve others daily.</p>
</div></div>

<div class="s sw"><div class="si sin">
<span class="lb">The Impact</span>
<h2>The Impact of Your Partnership</h2>
<p>When you support the Pastor and Missionary Rest &amp; Renewal Fund, you are helping to:</p>
<ul>
<li>Sustain pastors and missionaries for long-term faithfulness</li>
<li>Strengthen marriages and families in ministry</li>
<li>Encourage spiritual health and resilience</li>
<li>Protect gospel workers from burnout and isolation</li>
<li>Multiply impact through healthier leadership</li>
</ul>
<p>Your generosity creates ripple effects far beyond a single stay.</p>
</div></div>

<div class="s sl"><div class="si sin">
<span class="lb">For Churches</span>
<h2>For Churches and Missions Committees</h2>
<p>Churches and missions committees play a vital role in caring for those they send and support. The Pastor and Missionary Rest &amp; Renewal Fund offers churches a tangible way to invest in the long-term spiritual health of their pastors and missionaries. Rather than placing the responsibility for rest solely on individuals, this fund allows churches to:</p>
<ul>
<li>Encourage healthy rhythms of ministry and rest</li>
<li>Care for missionaries during furlough or transition</li>
<li>Provide space for pastoral reflection and prayer</li>
<li>Support families serving in demanding ministry contexts</li>
</ul>
<p>Many churches choose to support this fund as part of their broader commitment to shepherding those who shepherd others.</p>
