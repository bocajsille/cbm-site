<h1>Child Protection &amp; Safety</h1>
</div></div>
<div class="ib"><p>At CBM, we believe children are a gift from God and are entrusted to our care for a season. Child protection is not simply a legal requirement — it is a biblical responsibility. We are committed to creating environments where children are physically, emotionally, and spiritually safe.</p></div>

<div class="s sw"><div class="si sin">
<span class="lb">Our Commitment</span>
<h2>Our Commitment to Child Safety</h2>
<p>Our goal is not merely to prevent misconduct but to foster environments where children can learn, grow, and encounter Christ in safety and trust. Throughout Scripture, God calls His people to protect the vulnerable, care for the weak, and act with integrity and wisdom. Because of this, we strive to build a culture where safety, accountability, and respect are woven into every aspect of ministry.</p>
<p>At CBM, child protection is not a single policy or training event. It is an ongoing process built around four commitments:</p>
</div></div>

<div class="s sl"><div class="si">
<span class="lb">The Four Commitments</span>
<h2>How We Protect Children</h2>
<div class="commitment-grid">
<div class="commitment-card">
<h3><span>1</span> Screen</h3>
<p>We carefully screen individuals before they are permitted to serve with children. Depending on the role, this may include written applications, personal and ministry references, interviews with ministry leadership, and criminal background checks.</p>
</div>
<div class="commitment-card">
<h3><span>2</span> Train</h3>
<p>Every staff member and volunteer serving with children receives child protection training appropriate to their role — covering our Child Protection Policy, Code of Conduct, recognizing signs of abuse, healthy boundaries, supervision practices, and reporting responsibilities.</p>
<p>Our training emphasizes the <strong>"3 Ws"</strong> (Who is interacting, Where interactions take place, What is occurring) and the <strong>"3 Rs"</strong> (Recognize, Respond, Report).</p>
</div>
<div class="commitment-card">
<h3><span>3</span> Monitor</h3>
<p>We actively monitor ministry activities through clear standards: observable and interruptible environments, avoiding one-on-one interactions in private settings, appropriate supervision, teaching children to recognize appropriate and inappropriate touch and communication, and zero tolerance for bullying or harassment.</p>
</div>
<div class="commitment-card">
<h3><span>4</span> Report</h3>
<p>If you see something concerning — boundary violations, inappropriate conduct, suspected abuse or neglect, policy violations, or any behavior that raises concern — we want to hear about it. Reports are taken seriously and reviewed promptly.</p>
<p>CBM complies with applicable reporting laws. Many of our staff and leaders serve as mandatory reporters. When required by law or when a child's safety may be at risk, reports will be made to appropriate authorities. We cooperate fully with law enforcement and child welfare professionals.</p>
</div>
</div>
</div></div>

<div class="s sw"><div class="si sin">
<span class="lb">Our Foundation</span>
<h2>Our Biblical Foundation</h2>
<p>We believe:</p>
<ul>
<li>Every child is created in the image of God</li>
<li>Children deserve protection, dignity, and care</li>
<li>Ministry leaders are called to steward trust faithfully</li>
<li>Truth, accountability, and transparency honor Christ</li>
<li>Protecting children is part of loving our neighbors and serving the Church</li>
</ul>
<p>Protecting children is not just a policy — it is a ministry responsibility and a reflection of our commitment to Christ-like care.</p>
<h2 style="margin-top:2.5rem;">Questions or Concerns?</h2>
<p>Creating safe ministry environments requires partnership between families, churches, leaders, and organizations. We welcome questions regarding our child protection practices and are committed to continual improvement as we seek to care well for every child entrusted to us.</p>
</div></div>

