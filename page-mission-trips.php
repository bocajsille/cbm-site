<div class="cbm-intro"><p>Children's Bible Ministries engages in short-term mission work as an extension of its long-term gospel ministry. Mission trips are not standalone events — they are strategic investments in ongoing relationships, established ministry contexts, and the sustained proclamation of God's Word to children and youth.</p></div>

<div class="s sw"><div class="si sin">
<span class="lbl">Philosophy</span>
<h2>Why CBM Does Mission Trips</h2>
<p>At CBM, mission trips exist to support and strengthen long-term ministry — not to replace it. Every short-term mission effort is connected to a sustained work already underway, ensuring that teams serve alongside established missionaries rather than independently of them.</p>
<p>This philosophy reflects CBM's conviction that the Great Commission is accomplished through faithful, ongoing ministry presence rather than isolated events. Short-term teams come alongside long-term missionaries to encourage, support, serve, and share in the work of proclaiming God's Word.</p>
<h3>What CBM Mission Trips Look Like</h3>
<p>Depending on the location and season, mission trip teams may participate in:</p>
<ul>
<li>Bible teaching and children's ministry programs</li>
<li>Camp ministry and summer outreach</li>
<li>Released Time Bible Education support</li>
<li>Facility maintenance and camp improvement projects</li>
<li>Prayer walks, community outreach, and evangelism</li>
<li>Encouragement and support for full-time missionaries</li>
</ul>
</div></div>

<div class="s sl"><div class="si sin">
<span class="lbl">Where We Serve</span>
<h2>Domestic and International Opportunities</h2>
<p>CBM mission trip opportunities exist both within the United States and internationally, connected to CBM's established ministry network.</p>
<h3>United States</h3>
<p>Domestic mission trips often support CBM camps, Released Time Bible Education programs, and urban and rural outreach ministries. Teams serve alongside CBM missionaries in Tennessee, Florida, North Carolina, Virginia, Pennsylvania, Michigan, Georgia, Alabama, Maryland, and other states.</p>
<h3>International</h3>
<p>International mission opportunities are connected to CBM's global affiliates and partner ministries in Honduras, Moldova, Kenya, and the United Kingdom. International teams serve in environments shaped by existing long-term missionaries and established ministry relationships.</p>
<a href="<?php echo home_url('/scope-gospel-impact');?>" class="la">View CBM's Global Reach</a>
</div></div>

<div class="s sw"><div class="si sin">
<span class="lbl">Long-Term Fruit</span>
<h2>Connecting Short-Term Service to Long-Term Calling</h2>
<p>For many individuals, a mission trip with CBM becomes a defining moment in their spiritual journey. Service in established ministry contexts — alongside full-time missionaries, in camp environments, and among children and youth — often deepens a sense of calling and clarifies the direction of someone's life.</p>
<p>CBM believes that faithful short-term service is part of how God prepares future missionaries, ministry leaders, and lifelong supporters of gospel work. Some who serve on a mission trip eventually pursue the CBM Internship Program or the Missionary Pathway toward full-time service.</p>
